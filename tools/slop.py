def _speech_segs(vis_uri):
    """merged non-silence [t0,t1] segments of a viseme track (source-audio seconds)."""
    try:
        d=json.load(open(resolve_uri(vis_uri)))
        segs=[]
        for v in d.get("visemes",[]):
            if v.get("viseme")=="X": continue
            s,e=float(v["t0"]),float(v["t1"])
            if segs and s-segs[-1][1]<0.12: segs[-1][1]=e
            else: segs.append([s,e])
        return [s for s in segs if s[1]-s[0]>0.02] or None
    except Exception:
        return None

def _speech_segs_audio(wav_uri):
    """merged non-silence [t0,t1] segments straight from the take's WAV (20ms RMS gate, threshold
    relative to the take's loud level). PREFERRED over the viseme track: Rhubarb renders a soft
    inter-sentence pause as a near-closed mouth (viseme B), NOT silence (X) — that hid the real
    0.46s pause in short2's b01 and let the captions run through the sentence boundary."""
    try:
        import soundfile as sf, numpy as np
        x,sr=sf.read(resolve_uri(wav_uri))
        if getattr(x,"ndim",1)>1: x=x.mean(1)
        win=max(1,int(sr*0.02)); nw=len(x)//win
        if not nw: return None
        rms=np.sqrt((x[:nw*win].reshape(nw,win)**2).mean(1))
        thr=0.08*float(np.percentile(rms,95))
        segs=[]
        for i in range(nw):
            if rms[i]<thr: continue
            s,e=i*0.02,(i+1)*0.02
            if segs and s-segs[-1][1]<0.12: segs[-1][1]=e
            else: segs.append([s,e])
        return [s for s in segs if s[1]-s[0]>0.05] or None
    except Exception:
        return None

def _frac_to_t(segs, f):
    """map a fraction of total SPEECH time to a source timestamp (skips the silences)."""
    tot=sum(e-s for s,e in segs)
    want=max(0.0,min(1.0,f))*tot; acc=0.0
    for s,e in segs:
        if acc+(e-s)>=want-1e-9: return s+(want-acc)
        acc+=e-s
    return segs[-1][1]

def _sent_cuts(exps, segs):
    """place one cut per sentence boundary — at a real pause (speech resumes at the next seg) or
    'free' (char-proportional) — chosen GLOBALLY: prefer the most pause-cuts (a TTS take pauses at
    nearly every sentence break), then minimize Σ|sentence speech dur − its spoken-char share|.
    Absolute-distance windows can't do this: on staccato runs ("No window. No grey box. No title
    bar.") the char estimates drift past any fixed window and nearest-gap matching crosses
    assignments. A pause-cut is only admissible if the sentence it closes stays within a sane
    band of its expected duration (0.3×−3× +0.4s slack), which is what rejects a mid-sentence
    comma pause posing as a boundary. exps = per-SENTENCE expected speech durations (spoken-char
    share × total speech); returns one source-time per boundary."""
    durs=[e-s for s,e in segs]; T=sum(durs)
    cuts=[]; acc=0.0                      # (speech consumed before the cut, source time of resume)
    for i in range(len(segs)-1):
        acc+=durs[i]
        if segs[i+1][0]-segs[i][1]>=0.2: cuts.append((acc,segs[i+1][0]))
    K=len(exps)-1
    memo={}
    def best(k,cum,j):
        if k==K: return (0,-abs((T-cum)-exps[K]),())
        key=(k,round(cum,3),j)
        if key in memo: return memo[key]
        nf=min(cum+exps[k],T)             # free cut: exactly char-proportional, no pause consumed
        jj=j
        while jj<len(cuts) and cuts[jj][0]<=nf+1e-6: jj+=1
        fc=best(k+1,nf,jj)
        r=(fc[0],fc[1],(('f',nf),)+fc[2])
        lo,hi=max(0.05,0.3*exps[k]-0.2),3*exps[k]+0.4
        for i in range(j,len(cuts)):
            d=cuts[i][0]-cum
            if d<lo or d>hi: continue
            gc=best(k+1,cuts[i][0],i+1)
            r=max(r,(gc[0]+1,gc[1]-abs(d-exps[k]),(('g',i),)+gc[2]))
        memo[key]=r; return r
    out=[]
    for kind,v in best(0,0.0,0)[2]:
        out.append(cuts[v][1] if kind=='g' else _frac_to_t(segs,v/T))
    return out

def transcript_apply(p):
    """(re)build the r_transcript caption chunks from every VO line; returns chunk count."""
    rid="r_transcript"
    if rid in p["rows"]:
        for cid in list(p["rows"][rid]["clips"]): p["clips"].pop(cid,None)
        p["rows"][rid]["clips"]=[]
    else:
        p["rows"][rid]=OD([("type","caption"),("name","Transcript"),("params",OD()),("clips",[])])
        p["tracks"].insert(0,OD([("id","tk_transcript"),("name","Transcript"),("kind","video"),("rows",[rid])]))
    # float the transcript to its OWN track at the TOP of the stack — easiest for a human to find in the
    # editor, and it composites OVER the content (the big pop-in caption chunks sit above everything).
    ti=next((i for i,tk in enumerate(p["tracks"]) if rid in tk.get("rows",[])),None)
    if ti not in (None,0): p["tracks"].insert(0,p["tracks"].pop(ti))
    por = p.get("meta",{}).get("format")=="portrait"
    def span_has_content(t0,t1):
        """any real content (image/video/code/diagram, not a cover backdrop or a reaction OVERLAY) over
        [t0,t1]? Mirrors the editor's span_has_content — decides room-beat vs content-beat transcript y.
        A bg-desk scene with a cat-meme overlay reads as a ROOM beat (the overlay isn't a dodge-worthy card)."""
        for c2 in p["clips"].values():
            if p["rows"].get(c2.get("row",""),{}).get("type") not in ("image","video","code","diagram"): continue
            if c2.get("params",{}).get("layout")=="cover" or c2.get("params",{}).get("overlay"): continue
            if c2["start"]<t1 and c2["start"]+c2["dur"]>t0: return True
        return False
    def span_has_code(t0,t1):
        """a code card up over [t0,t1] → the transcript drops to CENTER (y=0), clear of the top-docked card."""
        for c2 in p["clips"].values():
            if p["rows"].get(c2.get("row",""),{}).get("type")!="code": continue
            if c2["start"]<t1 and c2["start"]+c2["dur"]>t0: return True
        return False
    def span_has_scene_bg(t0,t1):
        """a beat-authored backdrop (bNN_backdrop — not the compiled room bg, c_*) over [t0,t1]:
        the host stands fully in frame WITH a scene backdrop → its own caption band, tr_scene
        (owner's b02 ghost-girl tweak: -448, lower than the solo-room -673)."""
        for cid2 in p["rows"].get("r_bg",{}).get("clips",[]):
            c2=p["clips"].get(cid2)
            if c2 and not cid2.startswith("c_") and c2["start"]<t1 and c2["start"]+c2["dur"]>t0: return True
        return False
    ensure_anchors(p,["tr_room","tr_scene","tr_content","tr_code"])   # chunks ride these (Project panel)
    vys={}   # normalized dialog -> viseme uri (pairing fallback for non-skeleton ids)
    for ak,ad in p.get("assets",{}).items():
        if ad.get("type")=="visemes" and ad.get("uri"):
            vys.setdefault(_norm_text(ad.get("params",{}).get("dialog")), ad["uri"])
    n=0; nsrc={"wav":0,"viseme":0,"linear":0}
    for cid,c in sorted(p["clips"].items(), key=lambda kv: kv[1].get("start",0)):
        if p["rows"].get(c.get("row",""),{}).get("type")!="tts": continue
        spoken=c.get("params",{}).get("text","")
        chunks=_tr_chunks(c.get("params",{}).get("transcript") or spoken)
        if not chunks: continue
        rate=_clip_rate(p,c)
        vuri=None
        m=re.match(r"(b\d+)_vo$",cid)                    # the skeleton's paired viseme track
        av=p["clips"].get(f"{m.group(1)}_av") if m else None
        if av:
            ad=p.get("assets",{}).get(av.get("asset",""),{})
            vd=_norm_text(ad.get("params",{}).get("dialog",""))
            # ONLY trust the paired track if its dialog matches THIS line (or it stores none). A VO regen
            # can leave a STALE viseme paired (a different/shorter take) — then the char-fraction times the
            # captions to the WRONG speech, so they rush and the last one ends well before the audio.
            # A mismatch falls through to a text-matched track, else linear timing over the clip.
            if ad.get("type")=="visemes" and (not vd or vd in (_norm_text(spoken),_norm_text(_tts_norm(spoken)))):
                vuri=ad.get("uri")
        if not vuri: vuri=vys.get(_norm_text(spoken)) or vys.get(_norm_text(_tts_norm(spoken)))
        sad=p.get("assets",{}).get(c.get("asset") or "",{})   # the take's own wav = ground truth for pauses
        segs=_speech_segs_audio(sad["uri"]) if sad.get("type")=="speech" and sad.get("uri") else None
        src="wav" if segs else "viseme"
        if not segs: segs=_speech_segs(vuri) if vuri else None
        if not segs: src="linear"
        nsrc[src]+=1
        w0=float(c.get("params",{}).get("in") or 0.0)    # split take: its window of the audio
        w1=w0+c["dur"]*rate
        if segs:
            segs=[[max(s,w0),min(e,w1)] for s,e in segs if e>w0 and s<w1]
            segs=[s for s in segs if s[1]-s[0]>0.02] or None
        # chunk weights use the SPOKEN text (_tts_norm), not the display text — "2007" is four
        # characters on screen but "two thousand seven" out loud; raw-char weighting started every
        # chunk after a year too early (the short2 "Half your capture" desync).
        wl=[max(1,len(_tts_norm(ch))) for ch in chunks]
        tot=sum(wl)
        starts=[]
        if segs:
            # sentence anchors: char-fraction estimate, then SNAP to speech-resume after the nearest
            # real pause. TTS speaks each sentence at its own char rate, so the fraction alone lands
            # a sentence boundary early/late; the wav pause is exact. Chunks inside a sentence are
            # then char-fraction over THAT sentence's speech window, so drift can't cross a boundary.
            sents=[]; curs=[]
            for i,ch in enumerate(chunks):
                curs.append(i)
                if ch[-1] in ".!?…": sents.append(curs); curs=[]
            if curs: sents.append(curs)
            T=sum(e-s for s,e in segs)
            exps=[sum(wl[i] for i in s)/tot*T for s in sents]
            anchors=[segs[0][0]]
            for ts in (_sent_cuts(exps,segs) if len(sents)>1 else []):
                anchors.append(max(ts,anchors[-1]+0.05))
            anchors.append(segs[-1][1])
            for si,s in enumerate(sents):
                a0,a1=anchors[si],anchors[si+1]
                ss=[[max(x0,a0),min(x1,a1)] for x0,x1 in segs if x1>a0 and x0<a1]
                ss=[x for x in ss if x[1]-x[0]>0.02] or [[a0,max(a1,a0+0.05)]]
                stot=sum(wl[i] for i in s); sacc=0
                for i in s:
                    starts.append(round(c["start"]+(_frac_to_t(ss,sacc/stot)-w0)/rate,3)); sacc+=wl[i]
        else:
            acc=0
            for i,ch in enumerate(chunks):
                starts.append(round(c["start"]+0.1+(acc/tot)*max(0.0,c["dur"]-0.4),3)); acc+=wl[i]
        # the last caption holds until ~the audio end — it must not vanish during a trailing word or
        # pause (the align track can legitimately end before the audio ends). Was anchored to the last
        # viseme only, which left a visible gap. Capped just past the clip end.
        endl=c["start"]+(max((segs[-1][1]-w0)/rate+0.35, c["dur"]-0.05) if segs else c["dur"])
        starts.append(round(min(endl,c["start"]+c["dur"]+0.3),3))
        # Caption band = a project ANCHOR (offset stays 0 — tune the band per project, not per chunk):
        # ROOM/host beat → tr_room, TOP (she OWNS the frame; the bottom is her body + the Shorts
        # controls strip). Room WITH a scene backdrop (she stands fully in frame in a location,
        # e.g. the ghost-girl wallpaper shot) → tr_scene, a lower band (owner's b02 tweak). CODE
        # beat → tr_code, CENTER (clear of the top-docked card). Other content → tr_content
        # mid-upper; landscape → tr_content low band.
        room = por and not span_has_content(c["start"], c["start"]+c["dur"])
        code = por and span_has_code(c["start"], c["start"]+c["dur"])
        scene= room and span_has_scene_bg(c["start"], c["start"]+c["dur"])
        akey = (("tr_scene" if scene else "tr_room") if room else ("tr_code" if code else "tr_content")) if por else "tr_content"
        for i,ch in enumerate(chunks):
            last = i==len(chunks)-1
            prm=OD([("style","plain"),("text",_tr_display(ch)),("font_px",54 if por else 46),
                    ("align","center"),("box",True),("sfx",False),("anchor",akey),
                    ("transition",OD([("in","pop"),("out","fade" if last else False)]))])
            new_clip(p,"caption",rid,starts[i],round(max(0.35,starts[i+1]-starts[i]),3),
                     prm,f"{cid}_tr{i+1}")
        n+=len(chunks)
    if n: print(f"  timing: {nsrc['wav']} wav / {nsrc['viseme']} viseme / {nsrc['linear']} linear lines", file=sys.stderr)
    return n

def cmd_transcript(p, a):
    n=transcript_apply(p)
    save(p,a.out or a.project); print(f"transcript: {n} chunks on r_transcript")


# ── anchor: a caption MOVE HANDLE — one clip whose transform.pos shifts every caption/text
# clip STARTING inside its span (editor: caption_anchor_off; params.rows narrows the targets,
# default = every caption row). Because `transcript` rewrites only the r_transcript chunks,
# an anchor's offset SURVIVES a caption regen — author time-range caption moves HERE, never
# as per-chunk pos nudges (those are wiped on regen). Overlapping anchors sum.
def _beat_span(p, beat):
    ids=[cid for cid in p["clips"] if cid.startswith(beat+"_")]
    if not ids: sys.exit(f"no clips for beat {beat}")
    return (min(p["clips"][i].get("start",0) for i in ids),
            max(p["clips"][i].get("start",0)+p["clips"][i].get("dur",0) for i in ids))

def cmd_anchor(p, a):
    rid="r_capanchor"
    if a.rm:
        if a.rm not in p["clips"]: sys.exit(f"no clip {a.rm}")
        p["clips"].pop(a.rm)
        for r in p["rows"].values():
            if a.rm in r.get("clips",[]): r["clips"].remove(a.rm)
        save(p, a.out or a.project); print(f"removed {a.rm}"); return
    if a.pos is None:   # no --pos → list mode
        anchors=[(cid,p["clips"][cid]) for cid in p["rows"].get(rid,{}).get("clips",[])]
        if not anchors: print("no caption anchors"); return
        for cid,c in anchors:
            rows=c.get("params",{}).get("rows")
            print(f"{cid}: {c['start']:.2f}-{c['start']+c['dur']:.2f}  pos {c['transform']['pos']}"
                  + (f"  rows {rows}" if rows else ""))
        return
    if a.beat:
        b0,_,b1=a.beat.partition("..")
        t0=_beat_span(p,b0)[0]; t1=_beat_span(p,b1 or b0)[1]
    elif a.t0 is not None and a.t1 is not None:
        t0,t1=a.t0,a.t1
    else:
        sys.exit("need --beat bNN[..bMM] or --t0/--t1")
    prm=OD()
    if a.rows: prm["rows"]=[r.strip() for r in a.rows.split(",") if r.strip()]
    cid=new_clip(p,"anchor",rid,t0,t1-t0,prm,a.id)
    c=p["clips"][cid]
    c["transform"]["pos"]=[float(x) for x in a.pos.split(",")]
    c["notes"]="cap anchor"   # the editor's timeline label
    n=sum(1 for c2 in p["clips"].values()
          if p["rows"].get(c2.get("row",""),{}).get("type") in ("caption","text")
          and t0-1e-4 <= c2.get("start",0) < t1-1e-4
          and (not prm.get("rows") or c2.get("row") in prm["rows"]))
    save(p, a.out or a.project)
    print(f"anchor {cid}: {t0:.2f}-{t1:.2f}  pos {a.pos}  — moves {n} caption clip(s)")


def _ts(t, sep=","):   # seconds -> HH:MM:SS,mmm (srt) / HH:MM:SS.mmm (vtt)
    t=max(0.0,float(t)); h=int(t//3600); m=int(t%3600//60); s=int(t%60); ms=int(round((t-int(t))*1000))
    if ms==1000: s+=1; ms=0
    return f"{h:02d}:{m:02d}:{s:02d}{sep}{ms:03d}"

def caption_lines(p):
    """[(start, end, text)] for every spoken VO line in timeline order — the true transcript.
    Prefers params.transcript (reader-facing display text) over params.text (may be TTS-normalized)."""
    out=[]
    for cid,c in sorted(p["clips"].items(), key=lambda kv: kv[1].get("start",0)):
        if p["rows"].get(c.get("row",""),{}).get("type")!="tts": continue
        pr=c.get("params",{}) or {}
        txt=(pr.get("transcript") or pr.get("text") or "").strip()
        if not txt: continue
        s=float(c.get("start",0)); out.append((s, s+float(c.get("dur",0)), " ".join(txt.split())))
    return out

def captions_str(lines, fmt):
    if fmt=="txt":  return "\n".join(t for _,_,t in lines)+"\n"
    if fmt=="srt":  return "".join(f"{i}\n{_ts(s)} --> {_ts(e)}\n{t}\n\n" for i,(s,e,t) in enumerate(lines,1))
    if fmt=="vtt":  return "WEBVTT\n\n"+"".join(f"{_ts(s,'.')} --> {_ts(e,'.')}\n{t}\n\n" for s,e,t in lines)
    raise ValueError(fmt)

def cmd_captions(p, a):
    lines=caption_lines(p)
    if not lines: sys.exit("no VO lines found (is the cut compiled + voiced? `video.py voice`)")
    if a.sidecars:
        base=re.sub(r"\.slop\.json$","",a.project)
        for f,ext in (("txt",".captions.txt"),("srt",".srt"),("vtt",".vtt")):
            open(base+ext,"w",encoding="utf-8").write(captions_str(lines,f))
        print(f"wrote {base}.{{captions.txt,srt,vtt}}  ({len(lines)} lines)", file=sys.stderr)
    text=captions_str(lines, a.fmt)
    if a.out: open(a.out,"w",encoding="utf-8").write(text); print(f"wrote {a.out} ({len(lines)} lines)", file=sys.stderr)
    else:     sys.stdout.write(text)   # pipeable → e.g. `| clip.exe`

# ── bed: lay a music bed across a span. The mixer does NOT loop audio, so a bed longer than
# the source is REPEATED clips with a continuous source walk (params.in). Volume follows a
# keyframed RAMP (the music-lane automation the editor + export both honour): `--ramp
# "t:g,t:g,..."` = absolute project time : dB breakpoints, linearly interpolated (clamped to
# the endpoints), sampled onto per-clip gain_db keyframes. No ramp → a flat `--gain` bed.
#   e.g. a title-drop swell:  --gain -18 --ramp "26:-28,44:-28,47:-9,50:-9,53:-18"
#        a fade to silence:   --gain -16 --ramp "3:-16,22:-16,26:-60"
def _bed_default_gain(file, fallback=-18):
    """per-track default bed gain from library/music/catalogue.json (matched by file stem), else the
    fallback — lets a LOUD track carry its own quieter default (e.g. edm-detection-mode = -24 dB)."""
    try:
        stem=os.path.splitext(os.path.basename(str(file)))[0].lower()
        cat=json.load(open("library/music/catalogue.json"))
        for tr in cat.get("tracks",[]):
            if str(tr.get("name","")).lower()==stem and "gain_db" in tr: return float(tr["gain_db"])
    except Exception: pass
    return fallback

def cmd_bed(p, a):
    gain = a.gain if a.gain is not None else _bed_default_gain(a.file)   # catalogue per-track default
    t1=a.t1 if a.t1 is not None else total(p)
    r=subprocess.run(["ffprobe","-v","error","-show_entries","format=duration","-of","csv=p=0",
                      resolve_uri(a.file)],capture_output=True,text=True)
    try: sdur=float(r.stdout.strip())
    except ValueError:
        print("ffprobe failed on",a.file,"(run inside `nix develop`?)"); return
    akey="bed_"+re.sub(r"[^a-z0-9]+","",os.path.basename(a.file).rsplit(".",1)[0].lower())[:16]
    p.setdefault("assets",OD())[akey]=OD([("provider","external"),("type","music"),
        ("status","ready"),("uri",a.file),("meta",music_meta(a.file))])
    bps=[]
    if a.ramp:
        for tok in a.ramp.split(","):
            ts,_,gs=tok.partition(":"); bps.append((float(ts),float(gs)))
        bps.sort()
    def env(t):
        if not bps: return gain
        if t<=bps[0][0]: return bps[0][1]
        if t>=bps[-1][0]: return bps[-1][1]
        for i in range(len(bps)-1):
            (ta,ga),(tb,gb)=bps[i],bps[i+1]
            if ta<=t<=tb: return ga+(gb-ga)*(t-ta)/(tb-ta) if tb>ta else gb
        return gain
    src=0.0; i=1; made=[]; t=a.t0
    inner=[bt for bt,_ in bps if a.t0<bt<t1]
    idbase=f"c_{akey}_{int(round(a.t0))}"   # per-(file,start) id so re-using a song at several points can't collide
    while t<t1-0.05:
        take=min(t1-t, sdur-src); s0,s1=t,t+take
        cid=new_clip(p,"music","r_music",round(s0,3),round(take,3),
                     OD([("gain_db",round(env(s0),2)),("in",round(src,3))]),f"{idbase}{i:02d}")
        p["clips"][cid]["asset"]=akey
        if bps:   # gain keyframes: clip endpoints + breakpoints inside the clip (project time)
            kts=sorted(set([round(s0,3),round(s1,3)]+[bt for bt in inner if s0<bt<s1]))
            if max(env(k) for k in kts)-min(env(k) for k in kts)>0.05:
                p["clips"][cid]["keyframes"]=OD([("params.gain_db",
                    [OD([("t",round(k,3)),("v",round(env(k),2)),("interp","linear")]) for k in kts])])
        made.append(cid); i+=1; t+=take; src+=take
        if src>=sdur-0.05: src=0.0   # loop: continue from the top of the track
    save(p,a.out or a.project)
    print(f"bed: {len(made)} clip(s) of {os.path.basename(a.file)} over {a.t0:.1f}-{t1:.1f}s "
          f"(gain {a.gain} dB"+(f", ramp {a.ramp}" if a.ramp else "")+")")

# ── musicmeta: backfill title/artist/credit on every music asset from its ID3 tags ──
def cmd_musicmeta(p, a):
    """fill each MUSIC asset's title/artist/credit from its embedded tags (gaps only — a
    hand-edited value is kept). The title/artist drive the editor's on-screen 'now playing'
    chip; the credit line drives the export description. `--sidecar` also writes a
    <file>.meta.json next to each song so a fresh drag-in / re-lay keeps the metadata."""
    n=0; miss=[]
    for akey,asset in p.get("assets",OD()).items():
        if asset.get("type")!="music": continue
        uri=asset.get("uri","")
        before=json.dumps(asset.get("meta",{}),sort_keys=True,ensure_ascii=False)
        merged=music_meta(uri, asset.get("meta"))
        asset["meta"]=merged
        if not merged.get("title"): miss.append(os.path.basename(uri))
        if json.dumps(merged,sort_keys=True,ensure_ascii=False)!=before: n+=1
        print(f"  {akey}: {merged.get('title','(no tags)')}"+(f" — {merged['artist']}" if merged.get('artist') else ""))
        if getattr(a,"sidecar",False) and merged:
            try: json.dump({"meta":merged}, open(resolve_uri(uri)+".meta.json","w"), ensure_ascii=False, indent=2)
            except Exception as e: print("   (sidecar write failed:",e,")")
