PROJDIR = "."   # dir of the loaded project — plain uris resolve CWD-first, then here
                # (a project dir is portable: it can live outside the code repo)

def load(path):
    global PROJDIR
    PROJDIR = os.path.dirname(os.path.abspath(path))
    return json.load(open(path), object_pairs_hook=OD)

def resolve_uri(uri):
    """cache://→cache/, file:// stripped; plain relative paths try CWD then the project dir."""
    fp = (uri or "").replace("cache://", "cache/").replace("file://", "")
    if fp and not os.path.isabs(fp) and not os.path.exists(fp):
        pj = os.path.join(PROJDIR, fp)
        if os.path.exists(pj): return pj
    return fp
def save(p, path):
    json.dump(p, open(path,"w"), indent=2, ensure_ascii=False)

def jval(s):
    """parse a CLI value as JSON, falling back to a bare string."""
    try: return json.loads(s)
    except Exception: return s

def set_dotted(obj, dotted, value):
    """set a possibly-dotted key (params.text, transform.pos) into the clip dict."""
    parts = dotted.split(".")
    cur = obj
    for k in parts[:-1]:
        if k not in cur or not isinstance(cur[k], (dict, OD)): cur[k] = OD()
        cur = cur[k]
    cur[parts[-1]] = value

def order_index(p, rid):
    """draw-order index of a row (track order, top-first)."""
    flat = [r for t in p["tracks"] for r in t.get("rows",[])]
    return flat.index(rid) if rid in flat else 1e9

def ensure_row(p, rid):
    if rid in p["rows"]: return
    typ = next((t for t,r in ROW_OF.items() if r==rid), "image")
    p["rows"][rid] = OD([("type",typ),("name",rid),("params",OD()),("clips",[])])
    if not any(rid in t.get("rows",[]) for t in p["tracks"]):
        tid,name,kind = TRACK_OF.get(rid,(rid.replace("r_","tk_"),rid,"video"))
        p["tracks"].append(OD([("id",tid),("name",name),("kind",kind),("rows",[rid])]))

def ensure_row_after(p, rid, after, typ="image"):
    """like ensure_row but the new track slots right AFTER `after` in draw order (just below it),
    not at the bottom of the stack — overflow image rows (r_img2…) must stay in the content band."""
    if rid in p["rows"]: return
    p["rows"][rid]=OD([("type",typ),("name",rid),("params",OD()),("clips",[])])
    if not any(rid in t.get("rows",[]) for t in p["tracks"]):
        pos=next((i+1 for i,t in enumerate(p["tracks"]) if after in t.get("rows",[])),len(p["tracks"]))
        p["tracks"].insert(pos,OD([("id",rid.replace("r_","tk_")),("name",rid),("kind","video"),("rows",[rid])]))

def gen_id(p, typ):
    base = {"caption":"c_cap","text":"c_cap","code":"c_cd","shape":"c_sh","gradient":"c_vig",
            "image":"c_im","video":"c_vid","avatar":"c_av","tts":"v_","music":"c_mus","blur":"c_blur",
            "diagram":"c_diag","filler":"c_fill","anchor":"c_anc"}.get(typ,"c_")
    i=1
    while f"{base}{i}" in p["clips"]: i+=1
    return f"{base}{i}"

def new_clip(p, typ, row, start, dur, params, cid=None):
    cid = cid or gen_id(p, typ)
    ensure_row(p, row)
    c = OD([("row",row),("start",round(start,3)),("dur",round(dur,3)),
            ("params",OD(params)),
            ("transform",OD([("pos",[0,0]),("scale",[1.0,1.0]),("rot",0),("opacity",1),("anchor",[0.5,0.5])]))])
    if typ in ("image","video","avatar","tts","music"): c["asset"]=""
    p["clips"][cid]=c
    if cid not in p["rows"][row]["clips"]: p["rows"][row]["clips"].append(cid)
    return cid

def ensure_audio_row(p, rid="r_vidaud", name="Video Audio"):
    """An audio-kind row+track to hold video audio split out for advanced editing."""
    if rid not in p["rows"]:
        p["rows"][rid] = OD([("type","music"),("name",rid),("params",OD()),("clips",[])])
    if not any(rid in t.get("rows",[]) for t in p["tracks"]):
        p["tracks"].append(OD([("id","tk_vidaud"),("name",name),("kind","audio"),("rows",[rid])]))

# ── ripple (same semantics as the editor) ──
def shift_clip(c, d):
    c["start"] = round(c.get("start",0)+d, 3)
    for arr in c.get("keyframes",{}).values():
        for k in arr:
            if "t" in k: k["t"] = round(k["t"]+d, 3)

def ripple(p, pivot, d, except_id=None, after=True):
    for cid,c in p["clips"].items():
        if cid==except_id: continue
        s=c.get("start",0)
        if (s >= pivot-1e-6) if after else (s <= pivot+1e-6): shift_clip(c, d)

def rebase(p):
    if not p["clips"]: return 0
    mn=min(c.get("start",0) for c in p["clips"].values())
    if mn < -1e-6:
        for c in p["clips"].values(): shift_clip(c, -mn)
    return -mn if mn<0 else 0

def total(p): return max((c["start"]+c["dur"] for c in p["clips"].values()), default=0)

def add_asset(p, key, path):
    if key not in p["assets"]:
        p["assets"][key]=OD([("provider","external"),("type","image"),("status","ready"),("uri",path),("meta",OD())])

def sidecar_meta(uri):
    """asset meta from a `<file>.meta.json` sidecar (stock music attribution → the export's
    auto-credits read asset.meta.attribution.attribution_text — CC-BY must not get dropped)."""
    try:
        return OD(json.load(open(resolve_uri(uri)+".meta.json")).get("meta",{}))
    except Exception:
        return OD()

def _attribution_for(title, artist):
    """best-effort credit line. Our whole stock palette is Kevin MacLeod (incompetech, CC BY 4.0) →
    emit the canonical string that matches the existing sidecars; other artists get a plain
    "Title" by Artist the user finishes with a license (never assert a license we can't infer)."""
    if not title and not artist: return ""
    t = title or "Untitled"
    if (artist or "").strip().lower() == "kevin macleod":
        return f'"{t}" Kevin MacLeod (incompetech.com) — CC BY 4.0'
    return f'"{t}" by {artist}' if artist else f'"{t}"'

def id3_meta(uri):
    """read embedded audio tags (ID3 / Vorbis-comment) via ffprobe → an asset `meta` dict
    {title, artist, album, year, attribution:{attribution_text}}. Mirrors the editor's ID3 import
    so a song has the SAME credit whether it's dragged in or laid by a tool. {} on failure."""
    try:
        r=subprocess.run(["ffprobe","-v","error","-show_entries","format_tags:stream_tags",
                          "-of","json",resolve_uri(uri)],capture_output=True,text=True)
        j=json.loads(r.stdout or "{}")
        tags={}
        for tg in (j.get("format",{}).get("tags",{}), *[s.get("tags",{}) for s in j.get("streams",[])]):
            for k,v in (tg or {}).items():
                tags.setdefault(k.lower(), v)          # container tags win over per-stream
        m=OD()
        if tags.get("title"):  m["title"]=tags["title"]
        if tags.get("artist"): m["artist"]=tags["artist"]
        if tags.get("album"):  m["album"]=tags["album"]
        yr=tags.get("date") or tags.get("year") or ""
        if len(yr)>=4: m["year"]=yr[:4]
        att=_attribution_for(m.get("title",""), m.get("artist",""))
        if att: m["attribution"]=OD([("attribution_text",att)])
        return m
    except Exception:
        return OD()

def music_meta(uri, existing=None):
    """the meta a music asset should carry, layered low→high (later wins per non-empty key):
    ID3 tags (title/artist for the chip) < `.meta.json` sidecar (curated credit) < whatever's
    ALREADY on the asset (hand edits). So a stock bed whose sidecar only had an attribution line
    still GAINS title/artist from its tags, and a hand-tuned credit is never clobbered."""
    out=OD()
    for layer in (id3_meta(uri), sidecar_meta(uri), OD(existing or {})):
        for k,v in layer.items():
            if v: out[k]=v
    return out

