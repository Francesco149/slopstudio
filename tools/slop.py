def _expand_laughs(beats):
    """pre-split a leading-laugh beat ("Heh~ Welcome back, mortals") into TWO beats: a pure-giggle beat (the
    golden take + a smug closeup snap — see the laugh handling) followed by the SPEECH beat (normal TTS). The
    giggle is always a host closeup (it's HER giggling); the speech beat keeps the original visual/emotion/
    plate/etc. Non-line beats (sections/pauses) and whole-line laughs pass through untouched."""
    out = []
    for b in beats:
        sl = (_split_laugh(b.get("line", "")) if (isinstance(b, dict) and b.get("laugh") is None) else None)
        if not sl:
            out.append(b); continue
        laugh_txt, speech = sl
        out.append(OD([("line", laugh_txt), ("emotion", "smug"), ("laugh", True), ("visual", "host")]))
        sp = OD(b); sp["line"] = speech         # remainder speaks normally, keeping the beat's visual/emotion
        out.append(sp)
    return out

def _clip_rate(p, c):
    """effective playback rate of a tts clip: its own params.rate, else the project default
    (meta.speech_rate — the editor's mixer/export use the same fallback)."""
    r = c.get("params", {}).get("rate")
    if r is None: r = p.get("meta", {}).get("speech_rate", 1.0)
    try: return float(r) or 1.0
    except Exception: return 1.0

def _proj_gap(p):
    return 0.2 if p.get("meta", {}).get("format") == "portrait" else GAP

def _src_factor(p, c):
    """play→source seconds factor for timed media (mirrors the editor's src_time_factor):
    tts/music play at `rate`, video at `speed`; 0 for untimed clips (no in-point)."""
    rt = p.get("rows", {}).get(c.get("row", ""), {}).get("type")
    try:
        if rt == "tts": return min(2.0, max(0.5, _clip_rate(p, c)))
        if rt == "music": return min(2.0, max(0.5, float(c.get("params", {}).get("rate", 1.0)) or 1.0))
        if rt == "video": return float(c.get("params", {}).get("speed", 1.0)) or 1.0
    except Exception: return 1.0
    return 0.0

