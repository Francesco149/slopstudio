# ── retime: piecewise time-warp the WHOLE timeline so each beat matches its real VO duration ──
def cmd_retime(p, a):
    vos=sorted([(cid,c) for cid,c in p["clips"].items()
                if p["rows"].get(c.get("row",""),{}).get("type")=="tts"], key=lambda kv: kv[1]["start"])
    if not vos: print("retime: no VO clips"); return
    # a SPLIT take (params.in set, or several clips sharing one speech asset — the user cut a
    # line for a mid-sentence pause) is authored timing: retime must never resize it back to
    # the full audio duration (that corruption stranded the chiyo-key beat once already).
    shared={}
    for _,c in vos: shared[c.get("asset")]=shared.get(c.get("asset"),0)+1
    def is_split(c):
        return c.get("params",{}).get("in") is not None or shared.get(c.get("asset"),0)>1
    oldB=[c["start"] for _,c in vos]+[max(total(p), vos[-1][1]["start"]+vos[-1][1]["dur"])]
    newB=[oldB[0]]
    for i,(cid,c) in enumerate(vos):
        ad=p.get("assets",{}).get(c.get("asset",""),{})
        actual=None if is_split(c) else ad.get("meta",{}).get("duration")
        # the PLAYED duration at the clip's effective rate (shorts default ~1.3x via meta)
        if actual: actual=actual/_clip_rate(p,c)
        span=oldB[i+1]-oldB[i]
        newspan=span+(actual-c["dur"]) if actual else span
        # a --generate may have resized the VO clip in place WITHOUT rippling (span < audio) —
        # clamp the beat span so consecutive lines can never overlap
        if actual and i<len(vos)-1: newspan=max(newspan, actual+_proj_gap(p))
        newB.append(round(newB[-1]+newspan,3))
    def warp(t):
        for i in range(len(oldB)-1):
            if t<=oldB[i+1]+1e-9 or i==len(oldB)-2:
                a0,a1,b0,b1=oldB[i],oldB[i+1],newB[i],newB[i+1]
                if t<oldB[0]: return t
                u=(t-a0)/(a1-a0) if a1>a0 else 0
                return b0+u*(b1-b0)
        return t
    n=0
    for cid,c in p["clips"].items():
        s,e=c["start"],c["start"]+c["dur"]
        ns,ne=warp(s),warp(e)
        if abs(ns-s)>1e-4 or abs(ne-e)>1e-4:
            for arr in c.get("keyframes",{}).values():
                for k in arr:
                    if "t" in k: k["t"]=round(warp(k["t"]),3)
            c["start"],c["dur"]=round(ns,3),round(max(0.2,ne-ns),3); n+=1
    for cid,c in vos:   # VO clips get EXACT (rate-adjusted) audio duration; splits keep theirs
        if is_split(c): continue
        ad=p.get("assets",{}).get(c.get("asset",""),{})
        actual=ad.get("meta",{}).get("duration")
        if actual: c["dur"]=round(actual/_clip_rate(p,c),3)
    # ── OUTRO black-gap guard ─────────────────────────────────────────────────────────────────
    # If a --generate resized the LAST VO in place (its dur already == the new, longer audio), the
    # warp above saw no span change for the final beat, so it left the visuals + bed behind it at
    # their OLD (shorter) length → the outro line + its boom play over BLACK. Extend the last beat's
    # BACKING layers — the driven host, a cover backdrop, fillers, and the music bed — to cover the
    # VO. Timed content (captions/transcript/code/shapes/gag callouts) is left alone. (Fixed by hand
    # three times before this guard existed.)
    lc,lv=vos[-1]
    vo_end=round(lv["start"]+lv["dur"],3); beat0=lv["start"]; nx=0
    for cid,c in p["clips"].items():
        if cid==lc: continue
        rt=p["rows"].get(c.get("row",""),{}).get("type")
        cs,ce=c["start"],c["start"]+c["dur"]
        if ce<=beat0+1e-3 or cs>=vo_end-1e-3: continue          # not under the last beat
        lay=(c.get("params") or {}).get("layout")
        backing=(rt in ("avatar","music","blur")
                 or (rt in ("image","video") and lay=="cover")   # a cover plate = backdrop, not content
                 or "filler" in cid or "backdrop" in cid or "_bg" in cid)
        if backing and ce<vo_end-0.05:                           # a REAL gap (>50ms), not sub-frame rounding
            c["dur"]=round(vo_end-cs,3); nx+=1
    # SCENE CROSSFADE: when a FULL-FRAME shot (layout fullscreen/cover/fit) arrives contiguously after
    # another content clip, cross-DISSOLVE instead of hard-snapping (owner: "the whole scene snaps to the
    # new scene, maybe a smooth crossfade by default"). Extend the OUTGOING clip to linger XF into the
    # incoming AND fade it OUT over that tail; the incoming fades IN — a true dissolve that works whether the
    # two are on the SAME row (incoming draws over the lingering outgoing) or DIFFERENT rows (image-row →
    # video-row: fading BOTH means row z-order doesn't matter — the outgoing dissolves as the incoming
    # arrives). Contiguous INSET arrivals keep their spring POP (only a full-frame incoming triggers this);
    # backdrops (r_bg) + A-B loops are left alone. Runs AFTER the warp so the overlap survives.
    XF=0.4; nxf=0
    def _full(c): return ((c.get("params") or {}).get("layout")) in ("fullscreen","cover","fit")
    def _content(c): return p["rows"].get(c.get("row"),{}).get("type") in ("image","video") and c.get("row") not in ("r_bg","r_fill")
    allc=sorted([c for c in p["clips"].values() if _content(c)], key=lambda c:c["start"])
    for i in range(len(allc)-1):
        a0,b0=allc[i],allc[i+1]
        seam=b0["start"]-(a0["start"]+a0["dur"])
        if -0.12<seam<0.12 and _full(b0) and a0["dur"]>XF+0.4 and b0["dur"]>XF+0.4 \
           and not (a0.get("params") or {}).get("loop_out") and not (b0.get("params") or {}).get("loop_out"):
            a0["dur"]=round(a0["dur"]+XF,3)                    # outgoing lingers into the incoming
            prm=a0.setdefault("params",OD()); tr=prm.get("transition") or OD(); tr["out"]=OD([("type","fade"),("dur",XF)]); prm["transition"]=tr
            nxf+=1
    # portrait/shorts carry the animated transcript — regenerate it onto the fresh timing
    ntr=transcript_apply(p) if p.get("meta",{}).get("format")=="portrait" else 0
    save(p,a.out or a.project); print(f"retimed {n} clips against {len(vos)} VO lines -> total {total(p):.1f}s"
                                      + (f", extended {nx} outro backing" if nx else "")
                                      + (f", {nxf} scene crossfade" + ("s" if nxf!=1 else "") if nxf else "")
                                      + (f" (+{ntr} transcript chunks)" if ntr else ""))

# ── adopt: reuse GENERATED assets from another project by matching line text — a rebuild/remix
# keeps the hand-tuned VO takes (and their viseme tracks) instead of regenerating everything.
# Beat pairing: bNN_vo ↔ bNN_av (the skeleton compiler's ids); non-skeleton avatars match by overlap. ──
def _norm_text(t): return re.sub(r"\s+"," ",(t or "").strip())
def _spoken_two(n):
    ones=["zero","one","two","three","four","five","six","seven","eight","nine","ten","eleven","twelve",
          "thirteen","fourteen","fifteen","sixteen","seventeen","eighteen","nineteen"]
    tens=["","","twenty","thirty","forty","fifty","sixty","seventy","eighty","ninety"]
    return ones[n] if n<20 else tens[n//10]+("-"+ones[n%10] if n%10 else "")
def _tts_norm(t):
    """mirror of the editor's tts_normalize (speech_params): standalone 4-digit years → words,
    so lint/adopt can match authored text against the normalized text stored on generated assets."""
    def year(m):
        y=int(m.group(0)); hi,lo=y//100,y%100
        if 2000<=y<2010: return "two thousand "+_spoken_two(lo) if lo else "two thousand"
        if lo==0: return _spoken_two(hi)+" hundred"
        if lo<10: return _spoken_two(hi)+" oh "+_spoken_two(lo)
        return _spoken_two(hi)+" "+_spoken_two(lo)
    return re.sub(r"(?<![0-9A-Za-z])[12][0-9]{3}(?![0-9A-Za-z])", year, t or "")
def cmd_adopt(p, a):
    import shutil
    global PROJDIR
    dstdir=os.path.dirname(os.path.abspath(a.project))
    src=load(a.src)                       # NB: load() re-points PROJDIR at the SRC project dir…
    srcdir=PROJDIR
    speech={}; vis={}
    for ak,ad in src.get("assets",{}).items():
        if ad.get("status")!="ready" or not ad.get("uri"): continue
        if not os.path.exists(resolve_uri(ad["uri"])): continue   # stale entry whose cache file is gone
        if ad.get("type")=="speech":
            k=_norm_text(ad.get("params",{}).get("text"))
            speech.setdefault(k,(ak,ad)); speech.setdefault(_norm_text(_tts_norm(k)),(ak,ad))
        if ad.get("type")=="visemes": vis.setdefault(_norm_text(ad.get("params",{}).get("dialog")),(ak,ad))
    def install(ak,ad):
        # project dirs are portable: copy the cached file INTO this project (keeping its
        # "assets/…" relative uri) instead of carrying a uri that only resolves in the src dir
        ad=json.loads(json.dumps(ad), object_pairs_hook=OD)
        uri=ad["uri"]
        rel=uri.replace("cache://","cache/")
        sp=uri if os.path.isabs(uri) else os.path.join(srcdir,rel)
        if not os.path.exists(sp): sp=resolve_uri(uri)   # not localized into the src dir → the shared repo cache
        if not os.path.isabs(uri):
            dp=os.path.join(dstdir,rel)                  # cache:// lands in <proj>/cache/… (never a literal "cache:" dir)
            if not os.path.exists(dp):
                os.makedirs(os.path.dirname(dp),exist_ok=True); shutil.copy2(sp,dp)
        p.setdefault("assets",OD())[ak]=ad
    nv=na=0
    for cid,c in p["clips"].items():
        if p["rows"].get(c.get("row",""),{}).get("type")!="tts": continue
        key=_norm_text(c.get("params",{}).get("text"))
        hit=speech.get(key) or speech.get(_norm_text(_tts_norm(key)))
        if hit:
            ak,ad=hit; install(ak,ad); c["asset"]=ak; nv+=1
            m=re.match(r"(b\d+)_vo$",cid)   # pair the beat's avatar clip with the matching viseme track
            if m and key in vis and f"{m.group(1)}_av" in p["clips"]:
                vk,vd=vis[key]; install(vk,vd); p["clips"][f"{m.group(1)}_av"]["asset"]=vk; na+=1
    PROJDIR=dstdir
    save(p,a.out or a.project)
    print(f"adopted {nv} VO takes + {na} viseme tracks from {a.src} (genvo fills the rest)")

# ── genvo: bulk-generate every pending VO line + host viseme track via the headless editor,
# then retime. The one command between "skeleton compiled" and "watchable cut". ──
def cmd_genvo(a):
    exe=a.exe or "./build/slopstudio.exe"
    def _vo_text_for(p, av_cid, av_clip):
        """text of the VO a viseme track lip-syncs to: the beat-paired bNN_vo,
        else the tts clip with the most time overlap."""
        m=re.match(r"(b\d+)_av$",av_cid)
        if m and f"{m.group(1)}_vo" in p["clips"]:
            return p["clips"][f"{m.group(1)}_vo"].get("params",{}).get("text","")
        s,e=av_clip["start"],av_clip["start"]+av_clip["dur"]; best=None; bov=0
        for vid,vc in p["clips"].items():
            if p["rows"].get(vc.get("row",""),{}).get("type")!="tts": continue
            ov=min(e,vc["start"]+vc["dur"])-max(s,vc["start"])
            if ov>bov: bov=ov; best=vc.get("params",{}).get("text","")
        return best or ""
    for phase,rt in (("VO","tts"),("visemes","avatar")):
        p=load(a.project)   # reload — the editor writes the file after each gen
        todo=[]
        for cid,c in sorted(p["clips"].items(), key=lambda kv: kv[1].get("start",0)):
            if p["rows"].get(c.get("row",""),{}).get("type")!=rt: continue
            if rt=="tts" and not c.get("params",{}).get("text"): continue
            ak=c.get("asset"); ad=p.get("assets",{}).get(ak) if ak else None
            if ad and ad.get("status")=="ready" and ad.get("uri"):
                if os.path.exists(resolve_uri(ad["uri"])):
                    # a ready cache file — but reuse it ONLY if the text still matches. A
                    # reworded line (lint's STALE-VO) must regenerate, not play stale audio.
                    if rt=="tts":
                        ct=c.get("params",{}).get("text","")
                        at=ad.get("params",{}).get("text","")
                        if at in ("",ct,_tts_norm(ct)): continue   # unchanged → keep the take
                    else:
                        # viseme: stale if the line it lip-syncs to was reworded
                        dlg=ad.get("params",{}).get("dialog","")
                        vt=_vo_text_for(p,cid,c)
                        if not vt or dlg in ("",vt): continue
            todo.append(cid)
        print(f"genvo: {len(todo)} {phase} clips to generate")
        for i,cid in enumerate(todo):
            r=subprocess.run([exe,a.project,"--generate",cid],capture_output=True,text=True)
            ok = r.returncode==0
            print(f"  [{i+1}/{len(todo)}] {cid}: {'ok' if ok else 'FAILED'}")
            if not ok:
                tail = (r.stderr or r.stdout).strip().splitlines()
                print("    "+ (tail[-1] if tail else "(no output)"))
    p=load(a.project); cmd_retime(p, argparse.Namespace(out=a.project, project=a.project))

