# ── lint: catch the known LLM-authoring failure modes on ANY project ──
def _spans(p, rows):
    """merged active spans of clips on rows of the given types."""
    sp=[]
    for cid,c in p["clips"].items():
        rt=p["rows"].get(c.get("row",""),{}).get("type")
        if rt in rows: sp.append((c["start"], c["start"]+c["dur"]))
    sp.sort()
    out=[]
    for s,e in sp:
        if out and s<=out[-1][1]+1e-6: out[-1][1]=max(out[-1][1],e)
        else: out.append([s,e])
    return out

def cmd_lint(p, a):
    warn=[]; info=[]
    tot=total(p)
    vo=_spans(p,("tts",))
    vis=_spans(p,("image","video","code","caption","diagram","shape"))
    # 1. narration with NOTHING on screen but the host/bg (fine) — vs literally nothing
    allvis=_spans(p,("image","video","code","caption","diagram","shape","avatar","gradient","filler"))
    def gaps(cover, lo, hi, min_gap):
        g=[]; cur=lo
        for s,e in cover:
            if s-cur>min_gap: g.append((cur,s))
            cur=max(cur,e)
        if hi-cur>min_gap: g.append((cur,hi))
        return g
    for s,e in gaps(allvis,0,tot,0.25):
        warn.append(f"BLACK {s:7.2f}-{e:7.2f}  nothing at all on screen")
    for vs,ve in vo:
        for s,e in gaps([ (max(s0,vs),min(e0,ve)) for s0,e0 in vis if e0>vs and s0<ve ], vs, ve, 3.0):
            info.append(f"talky {s:7.2f}-{e:7.2f}  narration with no content visual (host-only; fine if intended)")
    # 2. visual repeats: same asset in many clips / back-to-back
    by_asset={}
    for cid,c in sorted(p["clips"].items(), key=lambda kv: kv[1].get("start",0)):
        if c.get("row")=="r_bg": continue      # the standing set backs many spans by design
        rt=p["rows"].get(c.get("row",""),{}).get("type")
        if rt in ("image","video") and c.get("asset"): by_asset.setdefault(c["asset"],[]).append(cid)
    for ak,cids in by_asset.items():
        if len(cids)>3: warn.append(f"REPEAT asset '{ak}' used by {len(cids)} clips: {', '.join(cids[:6])}")
    # 3. missing files — only for assets a clip actually REFERENCES (regens orphan old entries)
    refd={c.get("asset") for c in p["clips"].values() if c.get("asset")}
    for ak,ad in p.get("assets",{}).items():
        if ak not in refd: continue
        uri=ad.get("uri","")
        if not uri:
            if ad.get("status")!="ready": warn.append(f"ASSET '{ak}' not generated (status={ad.get('status')})")
            continue
        if not os.path.exists(resolve_uri(uri)): warn.append(f"MISSING '{ak}' -> {uri}")
    # 4. stale VO: clip text != generated asset text
    for cid,c in p["clips"].items():
        if p["rows"].get(c.get("row",""),{}).get("type")!="tts": continue
        ak=c.get("asset"); ad=p.get("assets",{}).get(ak) if ak else None
        ct=c.get("params",{}).get("text","")
        if ad and ct and ad.get("params",{}).get("text","") not in ("",ct,_tts_norm(ct)):
            warn.append(f"STALE-VO {cid}: clip text changed since gen ('{ct[:40]}...' vs asset)")
        if ad and ad.get("meta",{}).get("duration"):
            # a split take (params.in / shared asset) plays a WINDOW of the audio — check the
            # window fits instead of demanding dur == full audio (retime leaves splits alone)
            inpt=c.get("params",{}).get("in")
            nshare=sum(1 for c2 in p["clips"].values()
                       if c2.get("asset")==ak and p["rows"].get(c2.get("row",""),{}).get("type")=="tts")
            crate=_clip_rate(p,c)   # params.in is SOURCE seconds; dur plays dur*rate source seconds
            if inpt is not None or nshare>1:
                if (inpt or 0)+c["dur"]*crate > ad["meta"]["duration"]+0.25:
                    warn.append(f"SPLIT-OVERRUN {cid}: in {inpt or 0:.2f}+dur {c['dur']:.2f}x{crate:.2f} > audio {ad['meta']['duration']:.2f}s (plays silence)")
            elif abs(c["dur"]-ad["meta"]["duration"]/crate)>0.25:
                info.append(f"desync {cid}: dur {c['dur']:.2f}s vs audio {ad['meta']['duration']:.2f}s@{crate:.2f}x (run retime)")
        if not ad and ct: warn.append(f"NO-VO {cid}: line not generated yet ('{ct[:40]}')")
    # 4c. stale caption override: params.transcript no longer says the same THING as the (edited)
    # TTS text. The override exists for display normalization (digits, true spellings) — so aligned
    # word replacements that are digit-related or phonetically close (respells like
    # Resittear→Recettear) are fine; a low-similarity substitution ("anime"→"Lucky Star") means
    # someone reworded the spoken line and the invisible override kept the OLD wording (the
    # luckymas-short4 b01 bug, owner 2026-07-08).
    import difflib
    def _tr_stale(text, tr):
        strip=lambda w: re.sub(r"[^0-9a-zA-Z☆]+","",w).lower()
        tw=[w for w in text.split() if strip(w)]; rw=[w for w in tr.split() if strip(w)]
        sm=difflib.SequenceMatcher(None,[strip(w) for w in tw],[strip(w) for w in rw],autojunk=False)
        for op,i1,i2,j1,j2 in sm.get_opcodes():
            if op!="replace": continue
            aa="".join(strip(w) for w in tw[i1:i2]); bb="".join(strip(w) for w in rw[j1:j2])
            if any(ch.isdigit() for ch in aa+bb): continue   # digit normalization = the override's purpose
            if aa and bb and difflib.SequenceMatcher(None,aa,bb,autojunk=False).ratio()<0.4:
                return (" ".join(tw[i1:i2])," ".join(rw[j1:j2]))
        return None
    for cid,c in p["clips"].items():
        if p["rows"].get(c.get("row",""),{}).get("type")!="tts": continue
        pr=c.get("params",{}) or {}
        if pr.get("transcript") and pr.get("text"):
            st=_tr_stale(pr["text"],pr["transcript"])
            if st: warn.append(f"STALE-CAPTION {cid}: spoken '{st[0]}' but the caption override still says '{st[1]}' (params.transcript kept old wording?)")
    # 4b. clips starting before t=0 (export starts at 0 — they play truncated)
    for cid,c in p["clips"].items():
        if c.get("start",0) < -0.01: warn.append(f"NEGATIVE {cid} starts at {c['start']:.2f}s (before 0)")
    # 4d. SPATIAL — OFF-FRAME: a manually-positioned visual whose CENTER lands off the visible frame
    # (params.anchor base + transform.pos, in project px from the frame centre). High-signal for the
    # "aligns off-frame right" bug class (a nudge/anchor pushed a card or image past the edge). Excludes
    # avatars (the compositor adds render-time presenter/over-footage offsets not in the JSON), layout-
    # driven media (cover/fit/inset self-centre) and auto-placed captions (`place` = a corner offset).
    # Fuller spatial lint (host-over-footage, box overlap) needs the compositor's RESOLVED on-screen
    # boxes — a future editor `--dump-boxes` — so it isn't attempted from the JSON here.
    W,H=(p.get("meta",{}).get("resolution",[1920,1080])+[1920,1080])[:2]
    anchors=p.get("meta",{}).get("anchors",{})
    for cid,c in p["clips"].items():
        rt=p["rows"].get(c.get("row",""),{}).get("type")
        if rt not in ("image","video","diagram","shape"): continue
        pr=c.get("params",{}) or {}
        if pr.get("place"): continue                 # auto-cornered → pos is a corner-relative offset
        lay=pr.get("layout","")
        if lay in ("cover","fullscreen","fit") or lay.startswith("inset"): continue  # layout self-positions
        tf=c.get("transform",{}) or {}
        pos=(tf.get("pos",[0,0])+[0,0])[:2]
        ab=anchors.get(pr.get("anchor",""),[0,0])
        ox,oy=pos[0]+ab[0], pos[1]+ab[1]
        if abs(ox)>W*0.55 or abs(oy)>H*0.55:         # centre >10% past the frame edge (half = 0.5·dim)
            warn.append(f"OFF-FRAME {cid}: centre at ({ox:+.0f},{oy:+.0f}) px is off the {int(W)}x{int(H)} frame (pos {pos} + anchor {ab})")
    # 5. same-row overlaps — audio doubles up (warn); visuals may be intentional side-by-side (info)
    for rid,r in p["rows"].items():
        cs=sorted([p["clips"][c] for c in r.get("clips",[]) if c in p["clips"]], key=lambda c:c["start"])
        audio=r.get("type") in ("tts","music")
        for i in range(1,len(cs)):
            ov=cs[i-1]["start"]+cs[i-1]["dur"]-cs[i]["start"]
            if ov>0.05:
                m=f"OVERLAP on {rid}: clips overlap by {ov:.2f}s at {cs[i]['start']:.2f}"
                (warn if audio else info).append(m if audio else m+" (fine if side-by-side)")
    # 6. emotions that won't resolve in the rig (fall back to neutral — usually fine, listed FYI)
    rigs={r.get("params",{}).get("rig") for r in p["rows"].values() if r.get("type")=="avatar"}
    for rig in filter(None,rigs):
        mani=os.path.join("presets","avatars",rig,"manifest.json")
        if not os.path.exists(mani): continue
        exprs=set(json.load(open(mani)).get("expressions",{}).keys())
        missing={}
        for cid,c in p["clips"].items():
            if p["rows"].get(c.get("row",""),{}).get("type")!="avatar": continue
            emo=c.get("params",{}).get("emotion","")
            if emo and emo not in ("auto",) and emo.split(",")[0].strip() not in exprs:
                missing.setdefault(emo,[]).append(cid)
        for emo,cids in missing.items():
            info.append(f"emotion '{emo}' not in rig {rig} ({len(cids)} clips) -> falls back")
    for w in warn: print("WARN ",w)
    for i in info: print("info ",i)
    print(f"lint: {len(warn)} warnings, {len(info)} notes  ({len(p['clips'])} clips, {tot:.1f}s)")
    return 1 if warn else 0

# ── transcript: automated animated on-screen transcript (the shorts convention) ──
# One short caption chunk at a time popping in over the mid-band, timed onto each VO line:
# sentence boundaries SNAP to the real pauses in the take's WAV (20ms RMS gate — the viseme
# track is only a fallback; Rhubarb renders soft pauses as viseme B, not silence), and chunks
# inside a sentence are placed by SPOKEN-char fraction (_tts_norm: "2007" weighs as "two
# thousand seven") across that sentence's speech time. No wav/visemes → plain linear over
# the clip. Regenerable (wipes r_transcript first); portrait retime re-runs it,
# so genvo/retime keep it in sync. A line displays `params.transcript` when set — the
# authored `text` is what the TTS SPEAKS (phonetic spellings, "Heh~"…), the transcript is
# what the viewer should READ; they default to the same string.
TR_MAXCH = 17     # target chars per chunk (a big bold group on a 1080-wide portrait frame)
def _tr_display(s):
    """the ON-SCREEN transcript text: drop a trailing full-stop period only. A period at the end of a
    big shorts caption reads unnatural, but ? ! … , etc. carry TONE/context for a muted viewer, so
    they stay — as does an ellipsis (…/... = trailing-off). Chunk BOUNDARIES still use the original
    punctuation. (owner ask, narrowed to periods only.)"""
    s=(s or "").rstrip()
    return s[:-1] if (s.endswith(".") and not s.endswith("..")) else s

def _tr_chunks(text):
    chunks=[]; cur=""
    def flush():
        nonlocal cur
        if not cur: return
        if chunks and len(cur)<=8 and chunks[-1][-1] not in ".!?…":
            chunks[-1]+=" "+cur          # a short tail ("top.") rides the previous chunk
        else: chunks.append(cur)
        cur=""
    for w in (text or "").split():
        if cur and len(cur)+1+len(w)>TR_MAXCH: flush()
        cur=(cur+" "+w) if cur else w
        if cur[-1] in ".!?…": flush()    # sentence end = its own beat
    flush()
    return chunks

